package variable;

public class Var8 {
    public static void main(String[] args) {
        byte b = 127;
        short s = 32767;
        int i = 2147483647;

        long l = 9223372036854775807L;

        float f = 10.0f;
        double d = 10.0;

        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
    }
}
